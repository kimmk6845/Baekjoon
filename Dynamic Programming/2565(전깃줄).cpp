#include <iostream>
#include <algorithm>
#include <vector>
using namespace std;
#define MAXSIZE 102

int dp[MAXSIZE];
int main()
{
	// 가장 긴 증가하는 부분수열 이용
	// 왼쪽 전봇대를 오름차순으로 정렬 후 (N-부분수열값)을 이용
	// 6,1,2,3,4,5이면 6을 빼버리면 전기줄이 겹치지 않게 됨 --> 가장 긴 증가하는 부분수열을 이용하면 해결
	ios::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	int N, left, right;
	cin >> N;

	vector<pair<int, int>> v;
	v.emplace_back(0, 0);	// 더미, emplace_back은 push_back과 비슷하지만 성능 유리
	for (int i = 0; i < N; i++)
	{
		cin >> left >> right;
		v.emplace_back(left, right);
	}
	sort(v.begin(), v.end());	// left전봇대 오름차순 정렬

	int result = 0;
	for (int i = 1; i <= N; i++)
	{
		for (int j = 0; j < i; j++)
		{
			if (v[i].second > v[j].second)
				if (dp[j] >= dp[i]) dp[i] = dp[j] + 1;
		}
		result = max(result, dp[i]);
	}

	cout << N - result << "\n";
}

